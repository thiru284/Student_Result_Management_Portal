    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        String rollno = rollfield.getText();
        String physics = physicsfield.getText();
        String chemistry = chemistryfield.getText();
        String mathematics = mathematicsfield.getText();
        String dbms = dbmsfield.getText();
        String os = osfield.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","thiru284");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physics,chemistry,mathematics,dbms,os) values('"+rollno+"','"+physics+"','"+chemistry+"','"+mathematics+"','"+dbms+"','"+os+"')");
                JOptionPane.showMessageDialog(null, "Successfully updated the result");
                setVisible(false);
                new insertNewResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Roll number does not exit!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_savebtnActionPerformed

