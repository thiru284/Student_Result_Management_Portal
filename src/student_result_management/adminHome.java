    private void createbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtnActionPerformed
        // TODO add your handling code here:
        String course = (String)coursefield.getSelectedItem();
        String branch = (String)branchfield.getSelectedItem();
        String rollno = rollfield.getText();
        String name = namefield.getText();
        String gender = (String)genderfield.getSelectedItem();
        String fathername = fatherfield.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","thiru284");
            if(rollno.isEmpty()){
                JOptionPane.showMessageDialog(null, "Roll Number cannot be empty!");
            }
            else{
                Statement st = con.createStatement();
                st.executeUpdate("insert into student values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
                JOptionPane.showMessageDialog(null, "Successfully added a student");
                setVisible(false);
                new adminHome().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_createbtnActionPerformed

